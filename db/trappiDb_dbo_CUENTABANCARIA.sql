create table CUENTABANCARIA
(
    IDCUENTABANCARIA int           not null
        constraint PK_CUENTABANCARIA
            primary key nonclustered,
    IDCLIENTE        int           not null
        constraint FK_CUENTABA_RELATIONS_CLIENTES
            references CLIENTES,
    IDBANCO          int           not null
        constraint FK_CUENTABA_RELATIONS_BANCO
            references BANCO,
    NUMERODECUENTA   char(10)      not null,
    FONDOS           money,
    FECHAAPERTURA    varchar(1024) not null
)
go

